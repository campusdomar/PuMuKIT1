			} else {

				$criteria->add(BarWidgetPeer::WIDGET_TYPE_ID, $this->getId());

				BarWidgetPeer::addSelectColumns($criteria);
				$this->collBarWidgets = BarWidgetPeer::doSelect($criteria, $con);
			}
		} else {
			// criteria has no effect for a new object
			if (!$this->isNew()) {
				// the following code is to determine if a new query is
				// called for.  If the criteria is the same as the last
				// one, just return the collection.


				$criteria->add(BarWidgetPeer::WIDGET_TYPE_ID, $this->getId());

				BarWidgetPeer::addSelectColumns($criteria);
				if (!isset($this->lastBarWidgetCriteria) || !$this->lastBarWidgetCriteria->equals($criteria)) {
					$this->collBarWidgets = BarWidgetPeer::doSelect($criteria, $con);
				}
			}
		}
		$this->lastBarWidgetCriteria = $criteria;
		return $this->collBarWidgets;
	}

	/**
	 * Returns the number of related BarWidgets.
	 *
	 * @param      Criteria $criteria
	 * @param      boolean $distinct
	 * @param      Connection $con
	 * @throws     PropelException
	 */
	public function countBarWidgets($criteria = null, $distinct = false, $con = null)
	{
		// include the Peer class
		include_once 'lib/model/om/BaseBarWidgetPeer.php';
		if ($criteria === null) {
			$criteria = new Criteria();
		}
		elseif ($criteria instanceof Criteria)
		{
			$criteria = clone $criteria;
		}

		$criteria->add(BarWidgetPeer::WIDGET_TYPE_ID, $this->getId());

		return BarWidgetPeer::doCount($criteria, $distinct, $con);
	}

	/**
	 * Method called to associate a BarWidget object to this object
	 * through the BarWidget foreign key attribute
	 *
	 * @param      BarWidget $l BarWidget
	 * @return     void
	 * @throws     PropelException
	 */
	public function addBarWidget(BarWidget $l)
	{
		$this->collBarWidgets[] = $l;
		$l->setWidgetType($this);
	}

	/**
	 * Resets all collections of referencing foreign keys.
	 *
	 * This method is a user-space workaround for PHP's inability to garbage collect objects
	 * with circular references.  This is currently necessary when using Propel in certain
	 * daemon or large-volumne/high-memory operations.
	 *
	 * @param      boolean $deep Whether to also clear the references on all associated objects.
	 */
	public function clearAllReferences($deep = false)
	{
		if ($deep) {
			if ($this->collWidgets) {
				foreach ((array) $this->collWidgets as $o) {
					$o->clearAllReferences($deep);
				}
			}
			if ($this->collBarWidgets) {
				foreach ((array) $this->collBarWidgets as $o) {
					$o->clearAllReferences($deep);
				}
			}
		} // if ($deep)

		$this->collWidgets = null;
		$this->collBarWidgets = null;
	}


  public function __call($method, $arguments)
  {
    if (!$callable = sfMixer::getCallable('BaseWidgetType:'.$method))
    {
      throw new sfException(sprintf('Call to undefined method BaseWidgetType::%s', $method));
    }

    array_unshift($arguments, $this);

    return call_user_func_array($callable, $arguments);
  }


