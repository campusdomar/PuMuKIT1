	/**
	 * Selects a collection of Streamserver objects pre-filled with their StreamserverType objects.
	 *
	 * @return     array Array of Streamserver objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinStreamserverType(Criteria $c, $con = null)
	{
		$c = clone $c;

		// Set the correct dbName if it has not been overridden
		if ($c->getDbName() == Propel::getDefaultDB()) {
			$c->setDbName(self::DATABASE_NAME);
		}

		StreamserverPeer::addSelectColumns($c);
		$startcol = (StreamserverPeer::NUM_COLUMNS - StreamserverPeer::NUM_LAZY_LOAD_COLUMNS) + 1;
		StreamserverTypePeer::addSelectColumns($c);

		$c->addJoin(StreamserverPeer::STREAMSERVER_TYPE_ID, StreamserverTypePeer::ID);
		$rs = BasePeer::doSelect($c, $con);
		$results = array();

		while($rs->next()) {

			$omClass = StreamserverPeer::getOMClass();

			$cls = Propel::import($omClass);
			$obj1 = new $cls();
			$obj1->hydrate($rs);

			$omClass = StreamserverTypePeer::getOMClass();

			$cls = Propel::import($omClass);
			$obj2 = new $cls();
			$obj2->hydrate($rs, $startcol);

			$newObject = true;
			foreach($results as $temp_obj1) {
				$temp_obj2 = $temp_obj1->getStreamserverType(); //CHECKME
				if ($temp_obj2->getPrimaryKey() === $obj2->getPrimaryKey()) {
					$newObject = false;
					// e.g. $author->addBookRelatedByBookId()
					$temp_obj2->addStreamserver($obj1); //CHECKME
					break;
				}
			}
			if ($newObject) {
				$obj2->initStreamservers();
				$obj2->addStreamserver($obj1); //CHECKME
			}
			$results[] = $obj1;
		}
		return $results;
	}


